package zoo;

public class colorCodes {
    public colorCodes() {

    }

    public static String red() {
        return "\u001B[31m";
    }

    public static String green() {
        return "\u001B[32m";
    }

    public static String blue() {
        return "\u001B[34m";
    }

    public static String reset() {
        return "\u001B[0m";
    }

    public static String yellow() {
        return "\u001B[33m";
    }


}
